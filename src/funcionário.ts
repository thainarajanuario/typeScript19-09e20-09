export class funcionario{
    nome: string;
    idade: number;
    cargo: string;
    códFun: string;
    salário: string;


    constructor(
        nome: string,
        idade: number,
        cargo: string,
        códFun: string,
        salário: string,

){
    this.nome = nome
    this.idade = idade
    this.cargo = cargo
    this.códFun = códFun
    this.salário = salário

}
}